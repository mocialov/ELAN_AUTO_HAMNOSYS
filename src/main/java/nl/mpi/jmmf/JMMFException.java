package nl.mpi.jmmf;

public class JMMFException extends Exception {

	public JMMFException() {
		super();
	}

	public JMMFException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public JMMFException(String arg0) {
		super(arg0);
	}

	public JMMFException(Throwable arg0) {
		super(arg0);
	}

}
